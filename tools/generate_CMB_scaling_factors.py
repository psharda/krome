z = np.linspace(0,200,201)

if sp == 'co':
    nH = np.logspace(-2, 14, 201)
    #nH = np.array([1e4])
    #Tg1 = np.logspace(np.log10(3.0), 4, 31)
    Tg2 = 2.73*(1+z)
    Tg = Tg2 #np.sort(np.concatenate((Tg1, Tg2)))
    msp = 28
elif sp == 'c':
    nH = np.logspace(-2, 14, 31)
    Tg = np.logspace(np.log10(3.0), 4, 31)
    msp = 12
elif sp == 'c+':
    nH = np.logspace(-2, 14, 31)
    Tg = np.logspace(np.log10(3.0), 4, 31)
    msp = 12
elif sp == 'oh':
    nH = np.logspace(2, 12, 31)
    Tg = np.logspace(1, 3, 31)
    msp = 17
elif sp == 'oh2o':
    nH = np.logspace(-2, 14, 31)
    Tg = np.logspace(np.log10(3.0), 4, 31)
    msp = 18
else:
    raise ValueError('Species not yet supported')

vth = np.sqrt(2*1.38e-16*Tg/(msp*1.67e-24))
sigmav = vth

total = len(z) * len(nH) * len(Tg)
print('Total models to run: ', total, len(z), len(nH), len(Tg))

if LTE:
    filename = "cool"+sp.upper()+"_scalefactor_redshift_LTE.dat"
else:
    filename = "cool"+sp.upper()+"_scalefactor_redshift_NLTE.dat"

with open(filename, "w") as f, tqdm(total=total, desc="Running models for "+sp.upper()) as pbar:

    f.write(
        "# This file contains the table for "+sp.upper()+" cooling \n"
        "# scaling factors as a function of redshift \n"
        "# Generated by Piyush Sharda in 2025 using Despotic \n"
        "# The first three columns are the positions in the arrays \n"
        "# The 4th is the redshift \n"
        "# The 5th is log10(nH/cm^-3) \n"
        "# The 6th is log10(Tgas/K) \n"
        "# The last is the scaling factor to be multiplied to "+sp.upper()+" cooling \n"
        "# Note that the scaling factors for this file are calculated under LTE = "+str(LTE)+"\n\n"
    )


    # --- Step 1: Compute reference values at z=0 (serial) ---
    lambda_ref = np.zeros((len(nH), len(Tg)))
    for j in range(len(nH)):
        c.nH = nH[j]
        for k in range(len(Tg)):
            c.Tg = Tg[k]
            c.sigmaNT = sigmav[k]

            rhogas = 2.33*1.67e-24*nH[j]
            jeans_length = np.sqrt(np.pi*1.380648e-16*Tg[k]/rhogas/1.67e-24/6.674e-8/2.33)
            NH = 0.5*nH[j]*jeans_length
            c.colDen = NH
            try:
                lambda_ref[j][k] = np.log10(c.dEdt(LTE=LTE)['LambdaLine'][sp])
            except:
                lambda_ref[j][k] = -1e99
            pbar.update(1)

            # write reference row
            f.write('%d \t %d \t %d \t %.8e \t %.8e \t %d \t %.8e \n'%
                    (k+1, j+1, 1, np.log10(Tg[k]), np.log10(nH[j]), 0, lambda_ref[j][k]/lambda_ref[j][k]))
        f.write("\n")

