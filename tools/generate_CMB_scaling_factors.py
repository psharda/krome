c.noWarn = True

sp = str(input('enter specie name: '))
LTE = bool(input('For LTE, type 1. For NLTE, just press enter: ')) #not all species will converge at all (nH,Tg) under NLTE. One can play around with tolerances or reduced the number of transitions being solved for to make NLTE calculations possible for a larger subset of models.

print('LTE Selected: ', LTE)

